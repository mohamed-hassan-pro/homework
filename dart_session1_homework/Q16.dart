/*Question 16
Write a Dart code snippet that demonstrates the use of an arithmetic operator to
calculate the remainder of dividing 10 by 3.*/
void main() {
  int a = 10;
  int b = 3;
  int remainder = a % b; // % operator to find remainder
  print(remainder); // Output: 1 => 10/3=3.3333 => 0.3333*3=1
}
